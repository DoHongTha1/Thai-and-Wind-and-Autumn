package com.mycompany.app.week1;

public class SwitchCase2 {
    public static void Switchcase2 () {
        int day = 4;
        switch (day) {
            case 1:
                System.out.println("Saturday");
                break;
            case 2: 
                System.out.println("Sunday");
                break;
            default:
                System.out.println("Weekend");
        }
    }
}
